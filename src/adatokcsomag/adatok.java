    private void btnModositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModositActionPerformed
        // TODO add your handling code here:
        
        try {
            Connection conn = DriverManager.getConnection(host, user, password);
            String sql = "UPDATE mediumok SET "
                    + "elnevezes ='" + txtElnevezes.getText() + "',"
                    + "musorido ='" + txtMusorido.getText() + "',"
                    + "kozszolgalati ='" + txtKozszolg.getText() + "', "
                    + "elofizeteses ='" + txtElofiz.getText() + "' WHERE id =" + kivalasztottID;
                    
            Statement sta = conn.createStatement();
            sta.executeUpdate(sql);
            sta.close();
            conn.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Hiba az írás során!");
        }
        
        Feltolt();
        
    }//GEN-LAST:event_btnModositActionPerformed

