    private void Feltolt() {
        model = (DefaultTableModel) tblAdatok.getModel();
        model.setRowCount(0);
        
        try {
            Connection conn = DriverManager.getConnection(host, user, password);
            String sql = "SELECT * FROM mediumok";
            Statement sta = conn.createStatement();
            ResultSet rs = sta.executeQuery(sql);
            
            while (rs.next()) {                
                model.addRow(new Object[]{rs.getInt("id"), rs.getString("elnevezes"), rs.getInt("musorido"), 
                    rs.getString("kozszolgalati"), rs.getString("elofizeteses")});
            }
            sta.close();
            conn.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Hiba a lekérdezés során!");
        }
    }
